        public const double Celsius    =  -273.15;
        public const double Fahrenheit =  -523.67;
        public const double Rankine    =      0.0;
        public const double Delisle    =  559.725;
        public const double Newton     =   -90.14;
        public const double Reamur     =  -218.52;
        public const double Romer      =  -135.90;
    }
}
